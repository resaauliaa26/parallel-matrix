# MISD Example

data = 10

def algorithm_A(x):
    return x + 5

def algorithm_B(x):
    return x * 2

def algorithm_C(x):
    return x ** 2

result1 = algorithm_A(data)
result2 = algorithm_B(data)
result3 = algorithm_C(data)

print("Algorithm A:", result1)
print("Algorithm B:", result2)
print("Algorithm C:", result3)